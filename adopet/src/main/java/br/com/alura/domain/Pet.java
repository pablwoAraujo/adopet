package br.com.alura.domain;

public class Pet {

	private String kind;
	private String name;
	private String breed;
	private int age;
	private String color;
	private float weight;

	public Pet(String kind, String name, String breed, int age, String color, float weight) {
		this.kind = kind;
		this.name = name;
		this.breed = breed;
		this.age = age;
		this.color = color;
		this.weight = weight;
	}

}
